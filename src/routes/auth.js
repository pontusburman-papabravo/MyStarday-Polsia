// ─── Helper: complete login for a parent record ───────────────────
async function completeLogin(req, res, parent, userType) {
  // WHY: All modules already imported at top of file — but duplicated
  // here to keep the helper self-contained and avoid closure surprises.

  // Record login event for analytics
  db.query(
    'INSERT INTO login_event (user_id, role, family_id) VALUES ($1, $2, $3)',
    [parent.id, userType, parent.family_id]
  ).catch(() => {});

  const accessToken = jwt.sign(
    {
      id: parent.id,
      type: userType,
      familyId: parent.family_id,
      email: parent.email || null,
      isAdmin: parent.is_admin || false,
    },
    config.jwt.secret,
    { expiresIn: config.jwt.expiresIn }
  );

  const rawRefresh = await createRefreshToken({
    userId: parent.id,
    userType,
    familyId: parent.family_id,
  });
  setRefreshCookie(res, rawRefresh);

  const expiresInSecs = typeof config.jwt.expiresIn === 'string'
    ? parseDuration(config.jwt.expiresIn)
    : config.jwt.expiresIn;
  setAccessCookie(res, accessToken, expiresInSecs);

  const csrfToken = generateCsrfToken(res);

  const user = {
    id: parent.id,
    email: parent.email || null,
    familyId: parent.family_id,
    isAdmin: parent.is_admin || false,
    type: userType,
    onboarding_completed: parent.onboarding_completed,
  };

  const expiresAt = Date.now() + expiresInSecs * 1000;
  res.json({ csrfToken, user, expiresAt });
}

// ─── POST /api/auth/logout ────────────────────────────────
// Revoke the refresh token and clear cookies.
// When a child logs out, if a parent session was saved (via stjarndag_parent_session),
// restore it so the parent remains logged in.
router.post('/logout', async (req, res) => {
  try {
    const raw = req.cookies?.refresh_token;
    if (raw) {
      await revokeRefreshToken(raw);
    }
    // Explicitly clear access_token with exact matching attributes (path, secure, sameSite).
    // Using res.clearCookie directly (not clearAccessCookie) as a belt-and-suspenders approach
    // to ensure the cookie is deleted even if the helper has a subtle mismatch.
    res.clearCookie('access_token', {
      httpOnly: true,
      secure: process.env.NODE_ENV === 'production',
      sameSite: 'lax',
      path: '/',
    });
    res.clearCookie('refresh_token', {
      httpOnly: true,
      secure: process.env.NODE_ENV === 'production',
      sameSite: 'lax',
      path: '/api/auth',
    });
    // Clear CSRF cookie too
    res.clearCookie('csrf_token', { path: '/' });

    // ── Restore parent session only if CHILD is logging out ────────────────
    // Always decode access token from cookie to determine who is logging out.
    // Only restore the parent session when the child (not the parent) logs out.
    const accessTokenStr = req.headers.cookie?.split(';').find(c => c.trim().startsWith('access_token='))?.split('=').slice(1).join('=');
    const decoded = accessTokenStr ? jwt.decode(accessTokenStr) : null;
    if (decoded?.type !== 'child') {
      // Parent logout (or unknown type) — clear parent session cookie and do full logout
      res.clearCookie('stjarndag_parent_session', { path: '/' });
      return res.json({ message: 'Utloggad' });
    }

    const parentSessionCookie = req.cookies?.stjarndag_parent_session;
    if (parentSessionCookie) {
      let session;
      try {
        session = JSON.parse(Buffer.from(parentSessionCookie, 'base64').toString('utf8'));
      } catch {
        // Invalid cookie — just clear it
        res.clearCookie('stjarndag_parent_session', { path: '/' });
        return res.json({ message: 'Utloggad' });
      }

      if (session?.access_token && session?.refresh_token) {
        // Check if family has parent PIN — if so, keep session but require PIN first.
        // The frontend will show the PIN overlay before calling restore-parent-session.
        try {
          const familyResult = await db.query(
            'SELECT parent_pin_hash IS NOT NULL AS has_pin FROM family WHERE id = $1',
            [decoded.familyId]
          );
          if (familyResult.rows[0]?.has_pin) {
            // Parent PIN set — return needsParentPin: true so frontend shows PIN overlay.
            // Keep the parent session cookie intact (don't clear it).
            return res.json({ message: 'Utloggad', needsParentPin: true });
          }
        } catch (err) {
          console.error('[AUTH] Logout: parent-pin check failed:', err.message);
          // On error, fall through to auto-restore (favor usability)
        }

        // No parent PIN → auto-restore parent session
        res.cookie('access_token', session.access_token, {
          httpOnly: true,
          secure: process.env.NODE_ENV === 'production',
          sameSite: 'lax',
          maxAge: 15 * 60 * 1000,
          path: '/',
        });
        res.cookie('refresh_token', session.refresh_token, {
          httpOnly: true,
          secure: process.env.NODE_ENV === 'production',
          sameSite: 'lax',
          maxAge: 7 * 24 * 60 * 60 * 1000,
          path: '/api/auth',
        });
        res.clearCookie('stjarndag_parent_session', { path: '/' });
        return res.json({ message: 'Utloggad', sessionRestored: true });
      }
    }

    res.clearCookie('stjarndag_parent_session', { path: '/' });
    res.json({ message: 'Utloggad' });
  } catch (err) {
    console.error('[AUTH] Logout error:', err);
    // Still clear cookies even on error
    res.clearCookie('access_token', {
      httpOnly: true,
      secure: process.env.NODE_ENV === 'production',
      sameSite: 'lax',
      path: '/',
    });
    res.clearCookie('refresh_token', {
      httpOnly: true,
      secure: process.env.NODE_ENV === 'production',
      sameSite: 'lax',
      path: '/api/auth',
    });
    res.clearCookie('csrf_token', { path: '/' });
    res.clearCookie('stjarndag_parent_session', { path: '/' });
    res.json({ message: 'Utloggad' });
  }
});

